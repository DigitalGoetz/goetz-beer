package goetzbeer.data;

public class PlaceDetails {
	int featuredCount = 0;
	int onTapCount = 0;
	int bottleCount = 0;
	int canCount = 0;
	int growlerCount = 0;

	public int getFeaturedCount() {
		return featuredCount;
	}

	public void setFeaturedCount(int featuredCount) {
		this.featuredCount = featuredCount;
	}

	public int getOnTapCount() {
		return onTapCount;
	}

	public void setOnTapCount(int onTapCount) {
		this.onTapCount = onTapCount;
	}

	public int getBottleCount() {
		return bottleCount;
	}

	public void setBottleCount(int bottleCount) {
		this.bottleCount = bottleCount;
	}

	public int getCanCount() {
		return canCount;
	}

	public void setCanCount(int canCount) {
		this.canCount = canCount;
	}

	public int getGrowlerCount() {
		return growlerCount;
	}

	public void setGrowlerCount(int growlerCount) {
		this.growlerCount = growlerCount;
	}
}
